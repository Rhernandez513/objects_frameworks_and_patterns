package io.roberthernandez;

public class FacilityDetail {
    private String detail;

    public FacilityDetail(String s) {
        this.detail = s;
    }

    public void setDetail(String s) {
        this.detail = s;
    }

    public String getDetail() {
        return this.detail;
    }

    public String toString() {
        return this.detail;
    }
}
